
package Switch_Case;


public class Days_19 {
    public static void main(String[] args) {
         int Return = 7;

        switch (Return) {
            case 1:
                System.out.println("Saya memilih pilihan 1");
                break;
            case 2:
                System.out.println("Saya memilih pilihan 2");
                break;
            case 3:
                System.out.println("Saya memilih pilihan 3");
                break;
            default:
                System.out.println("Pilihan tidak valid");
        }
    }
}
