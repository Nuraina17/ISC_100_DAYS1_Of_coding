
package operasi;


public class OperasiAritmatika {
    public static void main(String[] args) {
        int angka1= 20;
        int angka2 = 5;
        
        int hasil;
        
        // 1.Penjumlahan
        
        hasil = angka1 + angka2; 
        System.out.println(angka1 + "+" + angka2 + "=" + hasil);
        
        // 2. Pengurangan
        
        hasil = angka1 - angka2;
        System.out.println(angka1 +  "-" + angka2 + "=" + hasil);
        
        // 3. Perkalian
        
        hasil = angka1 * angka2;
        System.out.println(angka1 +  "*" + angka2 + "=" + hasil);
        
        // 4. Pembagian
        
        hasil = angka1 / angka2;
        System.out.println(angka1 +  "/" + angka2 + "=" + hasil);
        
        // 5. Modulus
        
        hasil = angka1 % angka2;
        System.out.println(angka1 +  "%" + angka2 + "=" + hasil);
        
        
        
        
        
    }
}
